/*
 * mathtest.c -- boot-time known-answer-test battery for the fpm fixed-point
 * ========================================================================
 * math library (userspace/lib/fpm). Cryptotest-style: bare _start, own syscalls,
 * NO libc, NO floating point. Runs a COMPACT subset of the host KAT sweep on the
 * device -- table init, then 32 spot values per scalar function family plus a
 * vector normalize and a matrix inverse round-trip -- comparing against expected
 * Q16.16 integer constants precomputed on the host.
 *
 * WHY EXACT EQUALITY: fpm is integer-only and deterministic, and the device
 * links the SAME fpm.c the host generator ran (the freestanding pragmas change
 * only instruction selection / the canary, never integer results). So each
 * on-device result must equal the embedded expected value BIT-FOR-BIT. A wrong
 * or stubbed function diverges by thousands and is caught. The embedded tables
 * were generated by scratchpad/gen_mathtest.c (which #includes fpm.c) -- see the
 * MATH-0a report for the exact command.
 *
 * Prints one "[MATHTEST] <name>: PASS/FAIL" per family and a final
 * "MATHTEST: PASS n=<checks>" summary that build_test/mathtest_check.sh greps.
 */
/* Same freestanding gcc-15 pins as userspace/lib/fpm/fpm.c (see the rationale
 * block there): no auto-vectorizer xmm, no synthesized libc calls, no
 * re-injected %fs:0x28 canary (_start keeps fxm4 16-int locals on the stack,
 * which trips the stack-protector self-spec and would leave an undefined
 * __stack_chk_fail in this libc-free link), and no-sse so the by-value fxm4
 * copies expand to integer mov pairs instead of movdqa. Pragmas BEFORE the
 * include so fpm.h's static-inline vector helpers inherit them too. */
#pragma GCC optimize("no-tree-vectorize")
#pragma GCC optimize("no-tree-loop-distribute-patterns")
#pragma GCC optimize("no-stack-protector")
#pragma GCC target("no-sse")

#include "../../lib/fpm/fpm.h"

#define SYS_EXIT  0
#define SYS_WRITE 3

static long sc(long n, long a1, long a2, long a3) {
    long r;
    asm volatile("syscall" : "=a"(r) : "a"(n), "D"(a1), "S"(a2), "d"(a3)
                 : "rcx", "r11", "memory");
    return r;
}
static unsigned long slen(const char *s) { unsigned long n = 0; while (s[n]) n++; return n; }
static void print(const char *m) { sc(SYS_WRITE, 1, (long)m, (long)slen(m)); }
static void print_uint(unsigned int v) {
    char buf[12]; int i = 12;
    if (v == 0) { print("0"); return; }
    while (v && i > 0) { buf[--i] = (char)('0' + (v % 10)); v /= 10; }
    sc(SYS_WRITE, 1, (long)(buf + i), (long)(12 - i));
}
static void report(const char *name, int fails) {
    print("[MATHTEST] "); print(name); print(fails == 0 ? ": PASS\n" : ": FAIL\n");
}

