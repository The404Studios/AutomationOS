static void draw_char(int x, int y, unsigned char c, u32 fg, u32 bg) {
    if (c < 32 || c > 127) c = 32;
    c -= 32;
    for (int row = 0; row < 8; row++) {
        unsigned char bits = font8x8[c][row];
        for (int col = 0; col < 8; col++) {
            put_pixel(x + col, y + row, (bits & (0x80 >> col)) ? fg : bg);
        }
    }
}

static void draw_string(int x, int y, const char* s, u32 fg, u32 bg) {
    while (*s) {
        draw_char(x, y, *s, fg, bg);
        x += 9;
        s++;
    }
}

static usize strlen(const char* s) {
    usize len = 0;
    while (s[len]) len++;
    return len;
}

static void print(const char* msg) {
    syscall(SYS_WRITE, 1, (long)msg, strlen(msg));
}

void _start(void) {
    print("[COMPOSITOR] Starting AutomationOS compositor\n");

    fill_rect(0, 0, FB_WIDTH, FB_HEIGHT, 0xFF1A1A2E);

    fill_rect(0, 0, FB_WIDTH, 36, 0xFF16213E);

    fill_rect(0, FB_HEIGHT - 28, FB_WIDTH, 28, 0xFF16213E);

    fill_rect(0, 36, FB_WIDTH, 2, 0xFF0F3460);

    draw_string(20, 10, "AutomationOS Desktop", 0xFFFFFFFF, 0xFF16213E);

    draw_string(20, 60, "Press ESC to exit", 0xFF8899AA, 0xFF1A1A2E);

    draw_string(20, 80, "Compositor v1.0 running", 0xFF667788, 0xFF1A1A2E);

    print("[COMPOSITOR] Desktop rendered, entering event loop\n");

    while (1) {
        input_event_t ev;
        long ret = syscall(SYS_READ_EVENT, (long)&ev, sizeof(ev), 0);

        if (ret > 0 && ev.type == INPUT_EVENT_KEY && ev.value == KEY_STATE_PRESSED) {
            if (ev.code == KEY_ESC) {
                print("[COMPOSITOR] ESC pressed, shutting down\n");
                break;
            }
        }

        syscall(SYS_YIELD, 0, 0, 0);
    }

    print("[COMPOSITOR] Exiting\n");
    syscall(SYS_EXIT, 0, 0, 0);
}
