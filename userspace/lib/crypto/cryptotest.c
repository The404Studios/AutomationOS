/*
 * cryptotest.c -- known-answer self-test for the freestanding crypto lib.
 * =======================================================================
 * No libc, no syscalls, no malloc. Each test returns its own positive id on
 * failure so callers can identify the broken primitive; crypto_selftest()
 * returns 0 only when every vector matches.
 *
 * Failure id map:
 *    1  SHA-256("abc")
 *    2  SHA-1("abc")
 *    3  MD5("abc")
 *    4  HMAC-SHA256 RFC 4231 test case 1
 *    5  HMAC-SHA1  RFC 2202  test case 1
 *    6  AES-128 FIPS-197 single-block encrypt
 *    7  AES-128 FIPS-197 single-block decrypt (round-trip)
 *    8  AES-256 FIPS-197 single-block encrypt
 *    9  AES-128 CBC encrypt/decrypt round-trip
 *   10  AES-128-GCM NIST vector (encrypt: ct + tag)
 *   11  AES-128-GCM decrypt of vector 10 (tag verify + plaintext)
 *   12  AES-128-GCM decrypt with corrupted tag must FAIL
 */

#include "cryptotest.h"
#include "sha256.h"
#include "sha1.h"
#include "md5.h"
#include "hmac.h"
#include "aes.h"

/* constant-time-ish fixed-length compare; 1 if equal */
static int eq(const unsigned char *a, const unsigned char *b, unsigned long n) {
    unsigned long i;
    unsigned char d = 0;
    for (i = 0; i < n; i++) d |= (unsigned char)(a[i] ^ b[i]);
    return d == 0;
}

int crypto_selftest(void) {
    /* ---- 1. SHA-256("abc") ---------------------------------------- */
    {
        static const unsigned char msg[3] = { 'a', 'b', 'c' };
        static const unsigned char exp[32] = {
            0xba,0x78,0x16,0xbf,0x8f,0x01,0xcf,0xea,
            0x41,0x41,0x40,0xde,0x5d,0xae,0x22,0x23,
            0xb0,0x03,0x61,0xa3,0x96,0x17,0x7a,0x9c,
            0xb4,0x10,0xff,0x61,0xf2,0x00,0x15,0xad
        };
        unsigned char d[32];
        sha256(msg, 3, d);
        if (!eq(d, exp, 32)) return 1;
    }

    /* ---- 2. SHA-1("abc") ------------------------------------------ */
    {
        static const unsigned char msg[3] = { 'a', 'b', 'c' };
        static const unsigned char exp[20] = {
            0xa9,0x99,0x3e,0x36,0x47,0x06,0x81,0x6a,0xba,0x3e,
            0x25,0x71,0x78,0x50,0xc2,0x6c,0x9c,0xd0,0xd8,0x9d
        };
        unsigned char d[20];
        sha1(msg, 3, d);
        if (!eq(d, exp, 20)) return 2;
    }

    /* ---- 3. MD5("abc") -------------------------------------------- */
    {
        static const unsigned char msg[3] = { 'a', 'b', 'c' };
        static const unsigned char exp[16] = {
            0x90,0x01,0x50,0x98,0x3c,0xd2,0x4f,0xb0,
            0xd6,0x96,0x3f,0x7d,0x28,0xe1,0x7f,0x72
        };
        unsigned char d[16];
        md5(msg, 3, d);
        if (!eq(d, exp, 16)) return 3;
    }

    /* ---- 4. HMAC-SHA256 RFC 4231 test case 1 ---------------------- *
     * key = 0x0b x20, data = "Hi There"                              */
    {
        static const unsigned char key[20] = {
            0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,
            0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b
        };
        static const unsigned char data[8] = { 'H','i',' ','T','h','e','r','e' };
        static const unsigned char exp[32] = {
            0xb0,0x34,0x4c,0x61,0xd8,0xdb,0x38,0x53,
            0x5c,0xa8,0xaf,0xce,0xaf,0x0b,0xf1,0x2b,
            0x88,0x1d,0xc2,0x00,0xc9,0x83,0x3d,0xa7,
            0x26,0xe9,0x37,0x6c,0x2e,0x32,0xcf,0xf7
        };
        unsigned char d[32];
        hmac_sha256(key, 20, data, 8, d);
        if (!eq(d, exp, 32)) return 4;
    }

    /* ---- 5. HMAC-SHA1 RFC 2202 test case 1 ------------------------ *
     * key = 0x0b x20, data = "Hi There"                              */
    {
        static const unsigned char key[20] = {
            0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,
            0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b
        };
        static const unsigned char data[8] = { 'H','i',' ','T','h','e','r','e' };
        static const unsigned char exp[20] = {
            0xb6,0x17,0x31,0x86,0x55,0x05,0x72,0x64,0xe2,0x8b,
            0xc0,0xb6,0xfb,0x37,0x8c,0x8e,0xf1,0x46,0xbe,0x00
        };
        unsigned char d[20];
        hmac_sha1(key, 20, data, 8, d);
        if (!eq(d, exp, 20)) return 5;
    }

    /* ---- 6. AES-128 FIPS-197 C.1 single-block encrypt ------------- *
     * key = 000102...0f, pt = 00112233...ff, ct = 69c4e0d8...0a       */
    {
        static const unsigned char key[16] = {
            0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
            0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
        };
        static const unsigned char pt[16] = {
            0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,
            0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff
        };
        static const unsigned char exp[16] = {
            0x69,0xc4,0xe0,0xd8,0x6a,0x7b,0x04,0x30,
            0xd8,0xcd,0xb7,0x80,0x70,0xb4,0xc5,0x5a
        };
        aes_ctx c;
        unsigned char ct[16], rt[16];
        aes_set_encrypt_key(&c, key, 128);
        aes_encrypt_block(&c, pt, ct);
        if (!eq(ct, exp, 16)) return 6;

        /* ---- 7. round-trip decrypt ------------------------------- */
        aes_set_decrypt_key(&c, key, 128);
        aes_decrypt_block(&c, ct, rt);
        if (!eq(rt, pt, 16)) return 7;
    }

    /* ---- 8. AES-256 FIPS-197 C.3 single-block encrypt ------------- *
     * key = 0001...1f, pt = 00112233...ff, ct = 8ea2b7ca...89          */
    {
        static const unsigned char key[32] = {
            0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
            0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f,
            0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17,
            0x18,0x19,0x1a,0x1b,0x1c,0x1d,0x1e,0x1f
        };
        static const unsigned char pt[16] = {
            0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,
            0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff
        };
        static const unsigned char exp[16] = {
            0x8e,0xa2,0xb7,0xca,0x51,0x67,0x45,0xbf,
            0xea,0xfc,0x49,0x90,0x4b,0x49,0x60,0x89
        };
        aes_ctx c;
        unsigned char ct[16];
        aes_set_encrypt_key(&c, key, 256);
        aes_encrypt_block(&c, pt, ct);
        if (!eq(ct, exp, 16)) return 8;
    }

    /* ---- 9. AES-128 CBC round-trip ------------------------------- */
    {
        static const unsigned char key[16] = {
            0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,
            0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c
        };
        static const unsigned char iv0[16] = {
            0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
            0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
        };
        static const unsigned char pt[32] = {
            0x6b,0xc1,0xbe,0xe2,0x2e,0x40,0x9f,0x96,
            0xe9,0x3d,0x7e,0x11,0x73,0x93,0x17,0x2a,
            0xae,0x2d,0x8a,0x57,0x1e,0x03,0xac,0x9c,
            0x9e,0xb7,0x6f,0xac,0x45,0xaf,0x8e,0x51
        };
        /* NIST SP 800-38A F.2.1 first two ciphertext blocks */
        static const unsigned char exp_ct[32] = {
            0x76,0x49,0xab,0xac,0x81,0x19,0xb2,0x46,
            0xce,0xe9,0x8e,0x9b,0x12,0xe9,0x19,0x7d,
            0x50,0x86,0xcb,0x9b,0x50,0x72,0x19,0xee,
            0x95,0xdb,0x11,0x3a,0x91,0x76,0x78,0xb2
        };
        aes_ctx ce, cd;
        unsigned char iv[16], ct[32], rt[32];
        int i;

        aes_set_encrypt_key(&ce, key, 128);
        for (i = 0; i < 16; i++) iv[i] = iv0[i];
        aes_cbc_encrypt(&ce, iv, pt, ct, 2);
        if (!eq(ct, exp_ct, 32)) return 9;

        aes_set_decrypt_key(&cd, key, 128);
        for (i = 0; i < 16; i++) iv[i] = iv0[i];
        aes_cbc_decrypt(&cd, iv, ct, rt, 2);
        if (!eq(rt, pt, 32)) return 9;
    }

    /* ---- 10/11/12. AES-128-GCM (NIST GCM test vector, Case 4) ---- *
     * K   = feffe9928665731c6d6a8f9467308308                          *
     * IV  = cafebabefacedbaddecaf888 (12 bytes)                       *
     * AAD = feedfacedeadbeeffeedfacedeadbeefabaddad2 (20 bytes)       *
     * PT  = d9313225f88406e5a55909c5aff5269a86a7a953...b39 (60 bytes) *
     * CT  = 42831ec2217774244b7221b784d0d49c...91 (60 bytes)          *
     * Tag = 5bc94fbc3221a5db94fae95ae7121a47                          */
    {
        static const unsigned char key[16] = {
            0xfe,0xff,0xe9,0x92,0x86,0x65,0x73,0x1c,
            0x6d,0x6a,0x8f,0x94,0x67,0x30,0x83,0x08
        };
        static const unsigned char iv[12] = {
            0xca,0xfe,0xba,0xbe,0xfa,0xce,0xdb,0xad,
            0xde,0xca,0xf8,0x88
        };
        static const unsigned char aad[20] = {
            0xfe,0xed,0xfa,0xce,0xde,0xad,0xbe,0xef,
            0xfe,0xed,0xfa,0xce,0xde,0xad,0xbe,0xef,
            0xab,0xad,0xda,0xd2
        };
        static const unsigned char pt[60] = {
            0xd9,0x31,0x32,0x25,0xf8,0x84,0x06,0xe5,
            0xa5,0x59,0x09,0xc5,0xaf,0xf5,0x26,0x9a,
            0x86,0xa7,0xa9,0x53,0x15,0x34,0xf7,0xda,
            0x2e,0x4c,0x30,0x3d,0x8a,0x31,0x8a,0x72,
            0x1c,0x3c,0x0c,0x95,0x95,0x68,0x09,0x53,
            0x2f,0xcf,0x0e,0x24,0x49,0xa6,0xb5,0x25,
            0xb1,0x6a,0xed,0xf5,0xaa,0x0d,0xe6,0x57,
            0xba,0x63,0x7b,0x39
        };
        static const unsigned char exp_ct[60] = {
            0x42,0x83,0x1e,0xc2,0x21,0x77,0x74,0x24,
            0x4b,0x72,0x21,0xb7,0x84,0xd0,0xd4,0x9c,
            0xe3,0xaa,0x21,0x2f,0x2c,0x02,0xa4,0xe0,
            0x35,0xc1,0x7e,0x23,0x29,0xac,0xa1,0x2e,
            0x21,0xd5,0x14,0xb2,0x54,0x66,0x93,0x1c,
            0x7d,0x8f,0x6a,0x5a,0xac,0x84,0xaa,0x05,
            0x1b,0xa3,0x0b,0x39,0x6a,0x0a,0xac,0x97,
            0x3d,0x58,0xe0,0x91
        };
        static const unsigned char exp_tag[16] = {
            0x5b,0xc9,0x4f,0xbc,0x32,0x21,0xa5,0xdb,
            0x94,0xfa,0xe9,0x5a,0xe7,0x12,0x1a,0x47
        };
        aes_ctx c;
        unsigned char ct[60], rt[60], tag[16], badtag[16];
        int i;

        aes_set_encrypt_key(&c, key, 128);

        /* encrypt */
        aes_gcm_encrypt(&c, iv, aad, 20, pt, 60, ct, tag);
        if (!eq(ct, exp_ct, 60)) return 10;
        if (!eq(tag, exp_tag, 16)) return 10;

        /* decrypt with valid tag */
        if (aes_gcm_decrypt(&c, iv, aad, 20, ct, 60, tag, rt) != 0) return 11;
        if (!eq(rt, pt, 60)) return 11;

        /* decrypt with corrupted tag must fail */
        for (i = 0; i < 16; i++) badtag[i] = tag[i];
        badtag[0] ^= 0x01;
        if (aes_gcm_decrypt(&c, iv, aad, 20, ct, 60, badtag, rt) == 0) return 12;
    }

    return 0;
}
