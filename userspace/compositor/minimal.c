// Minimal freestanding compositor for AutomationOS
// Compiles with: x86_64-elf-gcc -ffreestanding -nostdlib -nostdinc -fno-builtin -fno-stack-protector -mno-red-zone -static -n

#define SYS_EXIT    0
#define SYS_READ    2
#define SYS_WRITE   3
#define SYS_READ_EVENT 14
#define SYS_YIELD   15

#define FB_BASE     ((volatile unsigned int*)0x40000000)
#define FB_WIDTH    1024
#define FB_HEIGHT   768

#define KEY_ESC             1
#define KEY_STATE_PRESSED   1
#define INPUT_EVENT_KEY     0

typedef unsigned long long u64;
typedef unsigned int       u32;
typedef unsigned short     u16;
typedef int                s32;
typedef unsigned long      usize;

typedef struct {
    u64 timestamp;
    u16 type;
    u16 code;
    s32 value;
} input_event_t;

static inline long syscall(long n, long a1, long a2, long a3) {
    long ret;
    asm volatile (
        "syscall"
        : "=a"(ret)
        : "a"(n), "D"(a1), "S"(a2), "d"(a3)
        : "rcx", "r11", "memory"
    );
    return ret;
}

static inline void put_pixel(int x, int y, u32 color) {
    if ((u32)x < FB_WIDTH && (u32)y < FB_HEIGHT)
        FB_BASE[y * FB_WIDTH + x] = color;
}

static void fill_rect(int x, int y, int w, int h, u32 color) {
    int x0 = x, y0 = y;
    int x1 = x + w, y1 = y + h;
    if (x0 < 0) x0 = 0;
    if (y0 < 0) y0 = 0;
    if (x1 > FB_WIDTH)  x1 = FB_WIDTH;
    if (y1 > FB_HEIGHT) y1 = FB_HEIGHT;
    for (int row = y0; row < y1; row++)
        for (int col = x0; col < x1; col++)
            FB_BASE[row * FB_WIDTH + col] = color;
}

