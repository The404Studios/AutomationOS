void _start(void)
{
    fpm_init();                 /* build the sine table before any trig KAT */
    int checks = 0, fails = 0, f;

    /* fx_mul */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_mul(MUL_A[i], MUL_B[i]) != MUL_E[i]) f++; }
    report("fx_mul (32 KAT, exact)", f); fails += f;

    /* fx_div (incl. one /0 -> saturate case at index 15) */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_div(DIV_A[i], DIV_B[i]) != DIV_E[i]) f++; }
    report("fx_div (32 KAT, incl /0 sat)", f); fails += f;

    /* fx_sqrt */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_sqrt(SQRT_I[i]) != SQRT_E[i]) f++; }
    report("fx_sqrt (32 KAT)", f); fails += f;

    /* fx_rsqrt */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_rsqrt(RSQRT_I[i]) != RSQRT_E[i]) f++; }
    report("fx_rsqrt (32 KAT)", f); fails += f;

    /* fx_sin */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_sin(SIN_B[i]) != SIN_E[i]) f++; }
    report("fx_sin (32 KAT)", f); fails += f;

    /* fx_cos */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_cos(SIN_B[i]) != COS_E[i]) f++; }
    report("fx_cos (32 KAT)", f); fails += f;

    /* fx_atan2 (all quadrants + axes) */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_atan2(ATAN_Y[i], ATAN_X[i]) != ATAN_E[i]) f++; }
    report("fx_atan2 (32 KAT)", f); fails += f;

    /* fx_asin */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_asin(ASIN_I[i]) != ASIN_E[i]) f++; }
    report("fx_asin (32 KAT)", f); fails += f;

    /* fx_acos */
    f = 0; for (int i = 0; i < 32; i++) { checks++; if (fx_acos(ASIN_I[i]) != ACOS_E[i]) f++; }
    report("fx_acos (32 KAT)", f); fails += f;

    /* fxv3 normalize + len */
    f = 0;
    { fxv3 v = fxv3_mk(NORM_IN[0], NORM_IN[1], NORM_IN[2]);
      fxv3 n = fxv3_normalize(v);
      checks++; if (n.x != NORM_EXP[0]) f++;
      checks++; if (n.y != NORM_EXP[1]) f++;
      checks++; if (n.z != NORM_EXP[2]) f++;
      checks++; if (fxv3_len(v) != LEN_EXP) f++; }
    report("fxv3 normalize+len (4 KAT)", f); fails += f;

    /* fxm4 inverse-affine round-trip: M * M^-1 ~= I (tolerance 0.01 in fx) */
    f = 0;
    { fxm4 M = fxm4_mul(fxm4_translate(fx_from_int(3), fx_from_int(-2), fx_from_int(5)),
                        fxm4_mul(fxm4_rotate_y(fpm_deg(37)),
                                 fxm4_scale(fx_from_int(2), fx_ratio(1,2), fx_from_int(3))));
      fxm4 Mi = fxm4_inverse_affine(M);
      fxm4 I  = fxm4_mul(M, Mi);
      const fx TOL = 655;   /* 0.01 in Q16.16 */
      for (int c = 0; c < 4; c++) for (int r = 0; r < 4; r++) {
          fx want = (c == r) ? FX_ONE : 0;
          fx d = I.m[c*4+r] - want; if (d < 0) d = -d;
          checks++; if (d > TOL) f++;
      } }
    report("fxm4_inverse_affine roundtrip (16)", f); fails += f;

    print("MATHTEST: ");
    if (fails == 0) { print("PASS n="); print_uint((unsigned)checks); print("\n"); }
    else            { print("FAIL fails="); print_uint((unsigned)fails);
                      print(" of "); print_uint((unsigned)checks); print("\n"); }

    sc(SYS_EXIT, 0, 0, 0);
    for (;;) {}
}
